      subroutine matm62(ty,wsp,cs,wsr,wst)

      include 'real8.h'

      save

      dimension tabat(4),tabz(22),tabl(21),tabt(22),tabp(22)

      logical init

      data init /.false./ ,ttysav /1.0d+30/

      data nz    /21/, 
     *    rhoz  /1.22500000d-03/, 
     *    tabat /8.31440000d+07, 6.36748800d+08, 9.80665000d+02, 
     *           2.89644000d+01/
      data tabz/
     * 0.            , 1.10190000d+06, 2.00630000d+06, 3.21620000d+06,
     * 4.73500000d+06, 5.24290000d+06, 6.15910000d+06, 7.99940000d+06,
     * 9.00000000d+06, 1.00000000d+07, 1.10000000d+07, 1.20000000d+07,
     * 1.50000000d+07, 1.60000000d+07, 1.70000000d+07, 1.90000000d+07,
     * 2.20000000d+07, 3.00000000d+07, 4.00000000d+07, 5.00000000d+07,
     * 6.00000000d+07, 7.00000000d+07/
      data tabl/
     *-6.49291769d-05, 9.28018576d-08, 9.86255062d-06, 2.77080373d-05,
     *-1.72248474d-07,-1.96000240d-05,-3.91696897d-05, 1.60821507d-07,
     * 2.98166740d-05, 5.02020080d-05, 9.97762300d-05, 2.00108809d-04,
     * 1.49589024d-04, 1.00407490d-04, 6.97598500d-05, 6.68801467d-05,
     * 3.49035000d-05, 3.31099360d-05, 2.58868500d-05, 1.71252960d-05,
     * 1.09162420d-05/
      data tabt/
     * 2.88150000d+02, 2.16604540d+02, 2.16688470d+02, 2.28621170d+02,
     * 2.70704137d+02, 2.70616652d+02, 2.52659110d+02, 1.80575130d+02,
     * 1.80736048d+02, 2.10552722d+02, 2.60754730d+02, 3.60530960d+02,
     * 9.60857386d+02, 1.11044641d+03, 1.21085390d+03, 1.35037360d+03,
     * 1.55101404d+03, 1.83024204d+03, 2.16134140d+03, 2.42020990d+03,
     * 2.59146286d+03, 2.70062528d+03/
      data tabp/
     * 1.01325000d+06, 2.26320000d+05, 5.47486994d+04, 8.68013979d+03,
     * 1.10904998d+03, 5.90004987d+02, 1.82098959d+02, 1.03769924d+01,
     * 1.64379881d+00, 3.00749781d-01, 7.35439451d-02, 2.52169805d-02,
     * 5.06169601d-03, 3.69429709d-03, 2.79259780d-03, 1.68519867d-03,
     * 8.67381898d-04, 1.94317430d-04, 4.15743157d-05, 1.13023464d-05,
     * 3.55894454d-06, 1.22936355d-06/

      if (init)goto 2

      re   = tabat(2)
      cons = re * re * tabat(3) * tabat(4) / tabat(1)
      trho = tabt(1) * rhoz * 1.0e3
      init = .true.

 2    tty = ty * 100.0d0
      j   = 1
      if (tty.le.0.0d0)goto 5

      do 3 j=2,nz
         if (tty - tabz(j))4,5,3
 3    continue

      j = nz + 1

 4    j = j - 1

 5    dum2  = (tabz(j) - tty) / ((re + tty) * (re + tabz(j)))
      dum3  = (re + tabz(j)) / (re + tty)
      var1  = (re + tabz(j)) * tabl(j) - tabt(j)
      rvar1 = 1.0d0 / var1
      var2  = ((tty - tabz(j)) * tabl(j) + tabt(j)) / tabt(j)
      fs    = -cons * (((tabl(j) * log(dum3 * var2)) * rvar1 +
     *   dum2) * rvar1)
      wsp   = tabp(j) * exp(fs)
      wst   = tabt(j) + tabl(j) * (tty - tabz(j))
      wsr   = trho * wsp / (wst * tabp(1))
      wsp   = 0.10d0 * wsp
      cs    = sqrt(1.4d0 * wsp / wsr)

      return
      end
